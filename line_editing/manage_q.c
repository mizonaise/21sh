#include "line_editing.h"

int			q_closed(char *str)
{
	int	i;
	int	q;
	int	dq;

	i = -1;
	q = 0;
	dq = 0;
	while (str[++i])
		if (DB_Q(str[i], dq) && !(i != 0 && str[i - 1] == '\\' && (q % 2) == 0))
		{
			q++;
		}
		else if (S_Q(str[i], q) && !(i != 0 && str[i - 1] == '\\'))
		{
			dq++;
		}
	return ((dq % 2 == 0) && (q % 2 == 0));
}
