#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char			*s;
	size_t			i;
	unsigned char	v;

	i = 0;
	v = c;
	s = str;
	while (i < n)
		s[i++] = v;
	return (str);
}
